package com.blah.recipes.model;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import com.fasterxml.jackson.annotation.JsonProperty;

import java.util.List;
import java.util.Objects;

@DynamoDBTable(tableName = "recipes")
public class Recipe {

    private String id;

    private String name;

    private List<Ingredient> ingredients;

    private Instructions instructions;

    public Recipe() {
    }

    public Recipe(String name, List<Ingredient> ingredients, Instructions instructions) {
        this.name = name;
        this.ingredients = ingredients;
        this.instructions = instructions;
    }

    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    @JsonProperty
    public String getId() {
        return id;
    }

    @JsonProperty
    public void setId(String id) {
        this.id = id;
    }

    @DynamoDBAttribute
    @DynamoDBIndexHashKey(globalSecondaryIndexName = "idx_global_name")
    @JsonProperty
    public String getName() {
        return name;
    }

    @JsonProperty
    public void setName(String name) {
        this.name = name;
    }

    @DynamoDBAttribute
    @JsonProperty
    public List<Ingredient> getIngredients() {
        return ingredients;
    }

    @JsonProperty
    public void setIngredients(List<Ingredient> ingredients) {
        this.ingredients = ingredients;
    }

    @DynamoDBAttribute
    @JsonProperty
    public Instructions getInstructions() {
        return instructions;
    }

    @JsonProperty
    public void setInstructions(Instructions instructions) {
        this.instructions = instructions;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Recipe recipe = (Recipe) o;
        return Objects.equals(name, recipe.name) &&
                Objects.equals(ingredients, recipe.ingredients) &&
                Objects.equals(instructions, recipe.instructions);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, ingredients, instructions);
    }

    @Override
    public String toString() {
        return "Recipe{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", ingredients=" + ingredients +
                ", instructions=" + instructions +
                '}';
    }
}
